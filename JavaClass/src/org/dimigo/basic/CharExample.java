package org.dimigo.basic;

public class CharExample {
public static void main(String[] args) {	
	char ch1 = 'A';
	char ch2 = 65;
	char ch3 = '\u0041';
	char ch4 = '가';
	String name = "김자바";
	System.out.println(ch1);
	System.out.println(ch2);
	System.out.println(ch3);
	System.out.println(ch4);
	System.out.print(name);
}
}